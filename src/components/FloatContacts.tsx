export default function FloatContacts() {
  const [open, setOpen] = useState(false);
  const wrapRef = useRef<HTMLDivElement>(null);

  useEffect(() => {
    function handle(e: MouseEvent) {
      if (wrapRef.current && !wrapRef.current.contains(e.target as Node)) {
        setOpen(false);
      }
    }
    if (open) document.addEventListener("mousedown", handle);
    return () => document.removeEventListener("mousedown", handle);
  }, [open]);

  return (
    <div className={styles.wrap} ref={wrapRef}>
      {/* Contact items — appear above the button */}
      <div className={`${styles.items} ${open ? styles.open : ""}`}>
        {contacts.map((c) => (
          <a
            key={c.id}
            href={c.href}
            className={styles.item}
            target={c.id !== "phone" ? "_blank" : undefined}
            rel={c.id !== "phone" ? "noopener noreferrer" : undefined}
            aria-label={c.label}
          >
            <span className={styles.label}>{c.label}</span>
            <span className={`${styles.icon} ${styles[c.colorClass as keyof typeof styles]}`}>
              {c.icon}
            </span>
          </a>
        ))}
      </div>

      {/* Toggle button */}
      <button
        className={`${styles.toggle} ${open ? styles.open : ""}`}
        onClick={() => setOpen((v) => !v)}
        aria-label={open ? "Закрыть контакты" : "Связаться с нами"}
        aria-expanded={open}
      >
        {/* Phone icon (default) */}
        <svg className={styles.iconMenu} viewBox="0 0 24 24">
          <path d="M6.6 10.8c1.4 2.8 3.8 5.1 6.6 6.6l2.2-2.2c.3-.3.7-.4 1-.2 1.1.4 2.3.6 3.6.6.6 0 1 .4 1 1V20c0 .6-.4 1-1 1C10.6 21 3 13.4 3 4c0-.6.4-1 1-1h3.5c.6 0 1 .4 1 1 0 1.3.2 2.5.6 3.6.1.3 0 .7-.2 1L6.6 10.8z" />
        </svg>
        {/* Close icon */}
        <svg className={styles.iconClose} viewBox="0 0 24 24">
          <path d="M18 6L6 18M6 6l12 12" stroke="#fff" strokeWidth="2.5" strokeLinecap="round" fill="none" />
        </svg>
      </button>
    </div>
  );
}
